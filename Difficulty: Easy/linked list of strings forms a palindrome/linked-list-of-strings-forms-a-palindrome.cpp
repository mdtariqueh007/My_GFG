class Solution {
    private:
    bool isPalindrome(string&s){
        int  i = 0;
        int j = s.length() - 1;
        
        while(i<=j){
            if(s[i]!=s[j]){
                return false;
            }
            i++;
            j--;
        }
        
        return true;
    }
  public:
    bool compute(Node* head) {
        // Your code goes here
        
        string temp = "";
        
        Node* curr = head;
        
        while(curr!=NULL){
            temp += curr->data;
            curr = curr->next;
        }
        // cout<<temp<<"\n";
        
        return isPalindrome(temp);
    }
};


