class Solution {
  public:
    int singleElement(int arr[] ,int N) {
        // code here
        int ans = 0;
        
        for(int i = 0;i<32;i++){
            int sum = 0;
            
            for(int j = 0;j<N;j++){
                sum += (arr[j]>>i)&1;
            }
            
            sum = sum%3;
            
            ans = ans | (sum<<i);
        }
        
        return ans;
    }
};

