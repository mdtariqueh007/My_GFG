class Solution {
  public:
    int swapNibbles(int n) {
        // code here
        int x = n & 0x0f;
        n = n>>4;
        int y = n & 0x0f;
        
        int ans = x;
        ans = ans<<4;
        
        return ans | y;
        
    }
};

